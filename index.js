function recuperarValores() {
    if (!JSON.parse(localStorage.getItem('DadosJogos'))) {
        return
    }
    jogos = JSON.parse(localStorage.getItem('DadosJogos'))
    for (let jogo of jogos) {
        const j1 = document.getElementById(`jogo${jogo.id}j1`);
        const j2 = document.getElementById(`jogo${jogo.id}j2`);
        if (!j1) continue
        if (!j2) continue
        document.getElementById(`jogo${jogo.id}j1`).value = jogo.time1.marcados
        document.getElementById(`jogo${jogo.id}j2`).value = jogo.time2.marcados

        //

        if (jogo.fase !== 'grupo') {
            document.getElementById(`jogo${jogo.id}t1`).innerHTML = jogo.time1.nome
            document.getElementById(`jogo${jogo.id}t2`).innerHTML = jogo.time2.nome
        }
    }
}

function atualizarFaseGrupo() {
    for (let jogo of jogos) {
        if (jogo.fase === 'grupo') {
            jogo.time1.marcados = document.getElementById(`jogo${jogo.id}j1`).value
            jogo.time2.marcados = document.getElementById(`jogo${jogo.id}j2`).value
        }
    }

    for (let jogo of jogos) {
        if (jogo.fase === "grupo") {
            if (jogo.time1.marcados == "") continue;
            if (jogo.time2.marcados == "") continue;

            jogo.time1.tomados = jogo.time2.marcados;
            jogo.time1.saldoGols = jogo.time1.marcados - jogo.time1.tomados;
            jogo.time1.pontuacao =
                jogo.time1.saldoGols > 0 ? 3 : jogo.time1.saldoGols == 0 ? 1 : 0;

            jogo.time2.tomados = jogo.time1.marcados;
            jogo.time2.saldoGols = jogo.time2.marcados - jogo.time2.tomados;
            jogo.time2.pontuacao =
                jogo.time2.saldoGols > 0 ? 3 : jogo.time2.saldoGols == 0 ? 1 : 0;
        }
    }

    localStorage.setItem('DadosJogos', JSON.stringify(jogos))
    location.reload()
}

function resolverEmpate(lista) {
    let rankingOriginal = lista;
    let empatadoPontuacao = [];
    let empatadoSaldoGols = [];
    let empatadoMarcados = [];

    for (let time of rankingOriginal) {
        //Se tiver alguem que a pontuação igualada ao primeiro, então adiconar a lista empatados pontuação
        if (time.pontuacao === lista[0].pontuacao) {
            empatadoPontuacao.push(time);
        }
    }

    //Se tiver mais de um time na lista de empatados por pontuação, então fazer nova verificação
    if (empatadoPontuacao.length > 1) {
        for (let time of empatadoPontuacao) {
            //Se tiver alguem que o saldo de gols igualado ao primeiro da lista de empatados por pontuação, então adiconar a lista empatadosSaldoGols
            if (time.saldoGols === lista[0].saldoGols) {
                empatadoSaldoGols.push(time);
            }
        }
        //Se tiver mais de um time na lista de empatados por saldoGols, então fazer nova verificação
        if (empatadoSaldoGols.length > 1) {
            for (let time of empatadoPontuacao) {
                //Se tiver alguem que os gols marcados iguala ao primeiro, então adiconar a lista empatadosMarcados
                if (time.saldoGols === lista[0].saldoGols) {
                    empatadoMarcados.push(time);
                }
            }
            if (empatadoMarcados.length > 1) {
                //Se tiver mais de um empatado nos gols marcados, então retorna resultado false e a lista somente com os empatados para fazer nova verificação posterior
                return {resultado: false, lista: empatadoMarcados};
            }

            //Se não houver empate com o primeiro por gols marcados (há campeão na disputa gols marcados), então retorna lista empateSaldoGols
            empatadoSaldoGols.sort((a, b) => b.marcados - a.marcados);
            return {resultado: true, lista: empatadoSaldoGols};
        }

        //Se não houver empate com o primeiro por SaldoGols (há campeão na disputa por saldoGols), então retorna empatadosPontuacao
        empatadoSaldoGols.sort((a, b) => b.marcados - a.marcados);
        empatadoSaldoGols.sort((a, b) => b.saldoGols - a.saldoGols);
        return {
            resultado: true,
            lista: empatadoPontuacao
        };
    }

    rankingOriginal.sort((a, b) => b.marcados - a.marcados);
    rankingOriginal.sort((a, b) => b.saldoGols - a.saldoGols);
    rankingOriginal.sort((a, b) => b.pontuacao - a.pontuacao);

    //Se não houver empate com o primeiro por Pontuação (há campeão na disputa por pontuação), então retorna lista original
    return ({
        resultado: true,
        lista: rankingOriginal
    });
}

